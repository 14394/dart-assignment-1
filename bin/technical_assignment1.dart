void main() {
  // string 
  String name = "Collins";
  print("Name: $name");

  // double (double)
  double height = 6.1;
  print("Height: $height");

  // int (int)
  int age = 23;
  print("Age: $age");

  // Boolean data type
  bool student = true;
  print("Is student: $student");

  // List data type
  List<int> numbers = [1, 2, 3, 4, 5];
  print("Numbers: $numbers");

  // Map data type
  Map<String, dynamic> person = {
    'name': 'Collins',
    'age': 23,
    'student': true
  };
  print("Person: $person");

}
